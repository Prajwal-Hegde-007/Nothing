public class EvenOrOdd {
    public static void main(String[] args) {
        // Predefined number to check
        int num = 10;  // Change this to any number you want

        if (num % 2 == 0) {
            System.out.println(num + " is even.");
        } else {
            System.out.println(num + " is odd.");
        }
    }
}
